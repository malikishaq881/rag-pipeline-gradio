# Cell 10 — Full RAG query function (entry point for Gradio)

NO_SOURCES_WARNING = (
    "⚠️ No relevant sources found.\n\n"
    "Try lowering MIN_SCORE or increasing TOP_K."
)

def format_sources(hits: List[Dict]) -> str:
    if not hits:
        return 'No sources.'
    lines = []
    for i, hit in enumerate(hits, 1):
        bar_len = int(hit['score'] * 20)
        bar     = '█' * bar_len + '░' * (20 - bar_len)
        preview = textwrap.shorten(hit['text'], width=200, placeholder=' [...]')
        lines.append(
            f"{'─'*60}\n"
            f"  #{i}  {hit['filename']}   chunk {hit['chunk_id']}\n"
            f"  Score : {hit['score']:.4f}  |{bar}|\n"
            f"  Text  : {preview}\n"
        )
    return '\n'.join(lines)

def rag_query(question: str, top_k: int, min_score: float) -> Tuple[str, str]:
    question = question.strip()
    if not question:
        return "Please enter a question.", ""
    hits = retrieve(question, top_k=int(top_k), min_score=float(min_score))
    if not hits:
        return NO_SOURCES_WARNING, 'No sources passed the similarity threshold.'
    answer  = generate_answer(question, hits)
    sources = format_sources(hits)
    return answer, sources

