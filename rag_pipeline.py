# Cell 6 — Embeddings + FAISS index  (model loaded ONCE)
# all-MiniLM-L6-v2: 22M params, 384-dim, fast and accurate.
# IndexFlatIP on L2-normalised vectors = cosine similarity.

EMBED_MODEL_NAME = "all-MiniLM-L6-v2"

print(f"Loading embedding model: {EMBED_MODEL_NAME} ...")
embed_model = SentenceTransformer(EMBED_MODEL_NAME)
print(f"✅ Embedding model ready  (dim={embed_model.get_sentence_embedding_dimension()})")

def build_faiss_index(chunks: List[Dict], model: SentenceTransformer):
    texts = [c['text'] for c in chunks]
    print(f"   Embedding {len(texts)} chunks ...")
    embeddings = model.encode(
        texts,
        batch_size=64,
        show_progress_bar=True,
        normalize_embeddings=True,
        convert_to_numpy=True,
    ).astype('float32')
    dim   = embeddings.shape[1]
    index = faiss.IndexFlatIP(dim)
    index.add(embeddings)
    print(f"✅ FAISS index built  ({index.ntotal} vectors, dim={dim})")
    return index, embeddings

faiss_index, chunk_embeddings = build_faiss_index(all_chunks, embed_model)

# Cell 7 — Retrieval
# Embed the query, search FAISS, filter by min_score.

def retrieve(query: str, top_k: int = 3, min_score: float = 0.4) -> List[Dict]:
    q_vec = embed_model.encode(
        [query], normalize_embeddings=True, convert_to_numpy=True
    ).astype('float32')
    scores, indices = faiss_index.search(q_vec, top_k)
    hits = []
    for score, idx in zip(scores[0], indices[0]):
        if idx < 0 or float(score) < min_score:
            continue
        c = all_chunks[idx]
        hits.append({
            'filename': c['filename'],
            'chunk_id': c['chunk_id'],
            'text':     c['text'],
            'score':    round(float(score), 4),
        })
    return hits

