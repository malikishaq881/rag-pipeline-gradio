# Cell 4 — Document Loading

def load_documents(data_dir: str) -> List[Dict]:
    """Load all .txt files. Returns List[{filename, text}]."""
    documents = []
    for path in sorted(Path(data_dir).glob('*.txt')):
        text = path.read_text(encoding='utf-8').strip()
        if text:
            documents.append({'filename': path.name, 'text': text})
            print(f"   [loaded] {path.name}  —  {len(text):,} chars")
    print(f"\n✅ Loaded {len(documents)} document(s)")
    return documents

documents = load_documents(DATA_DIR)

# Cell 5 — Chunking
# Overlapping character-level chunks. Each chunk: {chunk_id, filename, text}

CHUNK_SIZE = 400
OVERLAP    = 80

def chunk_document(doc: Dict, chunk_size: int = CHUNK_SIZE, overlap: int = OVERLAP) -> List[Dict]:
    text, filename = doc['text'], doc['filename']
    chunks, start, chunk_id = [], 0, 0
    while start < len(text):
        piece = text[start : start + chunk_size].strip()
        if piece:
            chunks.append({'chunk_id': chunk_id, 'filename': filename, 'text': piece})
            chunk_id += 1
        start += chunk_size - overlap
    return chunks

def chunk_all_documents(docs: List[Dict]) -> List[Dict]:
    all_chunks = []
    for doc in docs:
        chunks = chunk_document(doc)
        all_chunks.extend(chunks)
        print(f"   [chunked] {doc['filename']}  ->  {len(chunks)} chunks")
    print(f"\n✅ Total chunks: {len(all_chunks)}  (size={CHUNK_SIZE}, overlap={OVERLAP})")
    return all_chunks

